nome = 'carlos romulo vilela silva'

print(nome[0]) # acessar a posicao [0]

print(nome[:6]) # acessar o start e parar na posicao [6]

print(nome[10:]) #comecao da posicao [10] em diante

print(nome[10:16])# começa do [10] e vai até [16]

print(nome[10:16:2])# pega de dois em dois 

print(nome[:])# pega tudo 


print(nome[::-1])# embaralha

